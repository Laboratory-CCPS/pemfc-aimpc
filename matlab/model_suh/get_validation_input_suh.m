function [u, x0] = get_validation_input(Ts)

    t0 = 0;
    tend = 30;

    % Define control
    I_load = @(time) ...
            100 ...
            + 80 * (time >=  2) ... % 180 A
            + 40 * (time >=  6) ... % 220 A
            - 20 * (time >= 10) ... % 200 A
            + 60 * (time >= 14) ... % 260 A
            + 60 * (time >= 22) ... % 320 A
            ;
        
    v_cm = @(time) ...
        100 ...
            + 55 * (time >=  2) ...
            + 25 * (time >=  6) ...
            - 10 * (time >= 10) ...
            + 40 * (time >= 14) ...
            + 25 * (time >= 18) ...
            + 15 * (time >= 25) ...
            ;

    t = (t0:Ts:tend);
    u = [v_cm(t); I_load(t)];

    x0 = [0.1096e5; 0.7502e5; 5.4982e3; 1.4326e5];

end
